package com.paceup.OperatorsAndLoops;

public class IfElse {
	public static void main(String[] args) {
	      
        int n = 14;
        System.out.println(n>5);
        if (n > 5) {
            System.out.println("The number is greater than 5.");
        } else {
            System.out.println("The number is 5 or less.");            
        }
    }
}
