package com.paceup.day2;

class LogicalOperatorsExample
{
	public static void main (String[] args) 
	{

		// Logical operators
        boolean x = true;
        boolean y = true;
      
        System.out.println("x && y: " + (x && y));
        System.out.println("x || y: " + (x || y));
        System.out.println("!x: " + (!x));
        System.out.println("x"+x);

	}
}