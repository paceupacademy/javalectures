package com.paceup.day2;

class RelationalOperatorsExample
{
	public static void main (String[] args) 
	{

		// Comparison operators
		int a = 3;
		int b = 3;
		int c = 5;

		System.out.println("a > b: " + (a > b));
		System.out.println("a < b: " + (a < b));
		System.out.println("a >= b: " + (a >= b));
		System.out.println("a <= b: " + (a <= b));
		System.out.println("a == c: " + (a == c));
		System.out.println("a != b: " + (a != b));

	}
}