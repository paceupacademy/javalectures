package com.paceup.day2;

public class IfElse {
	public static void main(String[] args) {
	      
        int n = 4;

        if (n > 5) {
            System.out.println("The number is greater than 5.");
        } else {
            System.out.println("The number is 5 or less.");
            
        }
    }
}
